# Build BIOSSES ordered corpus + aligned id list
# Input: merged.jsonl, each line: {"id":"...","document_id":"...","text_1":"...","text_2":"...","label":"4.0"}
# Corpus is built from text_2; ids are taken from document_id (aligned with label_pool doc_ids)
