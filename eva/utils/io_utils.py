# Build Climate-FEVER ordered corpus + aligned id list
# Input: corpus.jsonl, each line: {"_id":"...", "title":"...", "text":"..."}
# Output:
#   ordered_corpus_climate.json
#   ordered_corpus_climate_ids.json  (aligned with above)
def build_climate_fever_corpus(
    src_corpus_fp: str | Path,
    out_corpus_fp: str | Path,
    out_ids_fp: str | Path,
    *,
    join_with: str = "\n",
) -> Tuple[List[str], List[str]]:
    src_corpus_fp = Path(src_corpus_fp)
    out_corpus_fp = Path(out_corpus_fp)
    out_ids_fp    = Path(out_ids_fp)

    arr = load_json_any(src_corpus_fp)  # JSONL/JSON compatible
    if not isinstance(arr, list):
        raise RuntimeError("climate_fever corpus.jsonl must be an object array or JSONL")

    texts: List[str] = []
    ids:   List[str] = []
    for it in arr:
        if not isinstance(it, dict):
            continue
        did   = str(it.get("_id", "")).strip()
        title = (it.get("title") or "").strip()
        text  = (it.get("text")  or "").strip()  # some entries have title only, no text
        combined = f"{title}{join_with}{text}" if title and text else (title or text)
        combined = (combined or "").strip()
        if combined:
            texts.append(combined)
            ids.append(did if did else "")
    out_corpus_fp.parent.mkdir(parents=True, exist_ok=True)
    out_ids_fp.parent.mkdir(parents=True, exist_ok=True)

    out_corpus_fp.write_text(json.dumps(texts, ensure_ascii=False), encoding="utf-8")
    out_ids_fp.write_text(json.dumps(ids,   ensure_ascii=False),   encoding="utf-8")
    print(f"✓ built climate ordered corpus: {out_corpus_fp} ({len(texts):,d}) + ids ({len(ids):,d})")
    return texts, ids

